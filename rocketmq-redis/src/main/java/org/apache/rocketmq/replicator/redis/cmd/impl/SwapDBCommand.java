package org.apache.rocketmq.replicator.redis.cmd.impl;

import org.apache.rocketmq.replicator.redis.cmd.Command;

/**
 * @author Leon Chen
 * @since 2.1.1
 */
public class SwapDBCommand implements Command {
    private int source;
    private int target;

    public SwapDBCommand() {

    }

    public SwapDBCommand(int source, int target) {
        this.source = source;
        this.target = target;
    }

    public int getSource() {
        return source;
    }

    public void setSource(int source) {
        this.source = source;
    }

    public int getTarget() {
        return target;
    }

    public void setTarget(int target) {
        this.target = target;
    }

    @Override
    public String toString() {
        return "SwapDBCommand{" +
                "source=" + source +
                ", target=" + target +
                '}';
    }
}
